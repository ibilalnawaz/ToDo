import Foundation
import UIKit

extension UIColor
{
    class func Blue() -> UIColor
    {
        return UIColor(red: 8.0/255.0, green: 217.0/255.0, blue: 214.0/255.0, alpha:1.0)
    }
    
    class func Red() -> UIColor
    {
        return UIColor(red: 255.0/255.0, green: 46.0/255.0, blue: 99.0/255.0, alpha:1.0)
    }
    class func DarkGrey() -> UIColor
    {
        return UIColor(red: 37.0/255.0, green: 42.0/255.0, blue: 52.0/255.0, alpha:1.0)
    }
    class func LightGrey() -> UIColor
    {
        return UIColor(red: 234.0/255.0, green: 234.0/255.0, blue: 234.0/255.0, alpha:1.0)
    }
}
